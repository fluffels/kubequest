/** Reihenfolge der Glyphen im Atlas (eine Zeile). Muss zur Zeichnung in scenes.ts passen. */
export const ATLAS_CHARS: string = Object.keys(GLYPHS).join("");

/** Hat die Font ein eigenes Glyph für dieses Zeichen? */
export function hasGlyph(ch: string): boolean {
  return Object.prototype.hasOwnProperty.call(GLYPHS, ch);
}

/** 7×5-bool-Raster einer Glyphe (Fallback `?`, sonst Leerzeichen, wenn auch das fehlt). */
export function glyphMatrix(ch: string): boolean[][] {
  const rows = GLYPHS[ch] ?? GLYPHS[FALLBACK_CHAR] ?? GLYPHS[" "];
  return rows.map((row) => {
    const cells: boolean[] = [];
    for (let x = 0; x < GLYPH_W; x++) cells.push(row[x] === "#");
    return cells;
  });
}

/** Ersetzt nicht vorhandene Zeichen, damit BitmapText nie auf eine fehlende Glyphe trifft.
 *  Bekannte Emojis (⚓ 🚪 🧭 ✨ 🪙) werden komplett entfernt – sie gehören nicht in die
 *  Textspur (das 🪙 ersetzt floatText() durch ein Pixel-Münz-Icon). Mehrfach-Leerzeichen,
 *  die dadurch entstehen, werden eingedampft, führende/abschließende getrimmt.
 *  Alle anderen unbekannten Zeichen werden zum sichtbaren Fallback `?`. */
export function sanitize(str: string): string {
  let out = "";
  for (const ch of str) {
    if (ch === " " || hasGlyph(ch)) out += ch;
    else if (DROP_CHARS.has(ch)) out += " "; // Emoji raus, Wortgrenze als Leerzeichen halten
    else out += FALLBACK_CHAR;
  }
  return out.replace(/ {2,}/g, " ").trim();
}

/** Emojis/Zeichen, die aus der Textspur entfernt (statt zu `?`) werden. */
const DROP_CHARS = new Set(["⚓", "🚪", "🧭", "✨", "🪙"]);
